function validateUrl(inputUrl) {
    console.log('::: Running validateUrl :::', inputUrl);

    const valid = /^(?:http(s)?:\/\/)?[\w.-]+(?:\.[\w\.-]+)+[\w\-\._~:/?#[\]@!\$&'\(\)\*\+,;=.]+$/.test(inputUrl);

    if(!valid) {
        alert('Your input is no valid url!');
    }
    return valid;
}

export { validateUrl };
